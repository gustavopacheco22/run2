    private void btnNewCorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewCorActionPerformed
        CorredorVO corvo = new CorredorVO();
        CorredorDAO cordao = new CorredorDAO();
        CategoriaVO catvo = new CategoriaVO();
        CategoriaDAO catdao = new CategoriaDAO();

        int j = 1;
        String nombre = txtNombreCor.getText();
        String apellido = txtApellidoCor.getText();
        String DNI = (txtDNiCor.getText());
        int edad = Integer.parseInt(txtEdadDCor.getText());
        String numero = (txtNumeroCor.getText());
        String sexo = comboBoxSexCor.getSelectedItem().toString();
        int categoria = -1;

        ArrayList<CategoriaVO> list = catdao.Listar_CategoriaVO();
        if (list.size() > 0) {
            for(int i=0; i<list.size(); i++){
            catvo = list.get(i);
                if (edad >= catvo.getDesdeAnio() && edad <= catvo.getHastaAnio()) {
                categoria = catvo.getId_categoria();
                }
            }
        }

        try {
            if (validaciones.isString(nombre)) {
                corvo.setNombre(nombre);
                if (validaciones.isString(apellido)) {
                    corvo.setApellido(apellido);
                    if (validaciones.isNumber(DNI)) {
                        corvo.setDNI(Integer.parseInt(DNI));
                        if (validaciones.isNumber(String.valueOf(edad))) {
                            corvo.setEdad(edad);
                            if (validaciones.isNumber(numero)) {
                                corvo.setNumero(Integer.parseInt(numero));
                                corvo.setSexoCorredor(sexo);
                                corvo.setId_categoria(categoria);
                                corvo.setEstado(false);
                                corvo.setTiempo("00:00:00");

                                j = cordao.Agregar_CorredorVO(corvo);
                                if (j == 1) {
                                    JOptionPane.showMessageDialog(this, "Datos Agregados Correctamente");
                                    txtNombreCor.setText("");
                                    txtApellidoCor.setText("");
                                    txtDNiCor.setText("");
                                    txtEdadDCor.setText("");
                                    txtNumeroCor.setText("");
                                } else {
                                    JOptionPane.showMessageDialog(this, "Error al Agregar los Datos");
                                }
                            }else {
                                    JOptionPane.showMessageDialog(this, "Ingrese correctamente el numero");
                                    txtNumeroCor.setText("");
                            }
                        }else {
                                JOptionPane.showMessageDialog(this, "Ingrese correctamente la edad");
                                txtEdadDCor.setText("");
                        }
                    }else {
                            JOptionPane.showMessageDialog(this, "Ingrese correctamente el DNI");
                            txtDNiCor.setText("");
                    }
                }else {
                        JOptionPane.showMessageDialog(this, "Ingrese correctamente el DNI");
                        txtApellidoCor.setText("");
                }
            }else {
                    JOptionPane.showMessageDialog(this, "Ingrese correctamente el DNI");
                    txtNombreCor.setText("");
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnNewCorActionPerformed

